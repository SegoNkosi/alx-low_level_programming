#include <stdio.h>

/**
 * add - will  two integers
 * @a: the first unit
 * @b: the second unit
 * Return: result
 */

int add(int a, int b)
{
	return (a + b);
}

/**
 * sub - will subtract two integers
 * @a: the first unit
 * @b: the second unit
 * Return: result of difference
 */

int sub(int a, int b)
{
        return (a - b);
}

/**
 * mul - will multiply two integers
 * @a: the first unit
 * @b: the second unit
 * Return: result
 */

int mul(int a, int b)
{
        return (a * b);
}

/**
 * div - will divide two integers
 * @a: the first unit
 * @b: the second unit
 * Return: result of div
 */

int div(int a, int b)
{
        return (a / b);
}

/**
 * mod - modulus  two integers
 * @a: the first unit
 * @b: the second unit
 * Return: result
 */

int mod(int a, int b)
{
        return (a % b);
}
